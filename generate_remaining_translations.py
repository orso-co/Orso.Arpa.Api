def escape_sql(text):
    """Escape single quotes for SQL by doubling them"""
    return text.replace("'", "''")

def generate_sql(lang_code, lang_dict):
    """Generate SQL INSERT statements for a language"""
    sql_lines = []
    for instrument in instruments:
        if instrument in lang_dict:
            translation = escape_sql(lang_dict[instrument])
            key = escape_sql(instrument)
            sql = f"INSERT INTO localizations (id, key, text, localization_culture, resource_key, created_by, created_at) VALUES (gen_random_uuid(), '{key}', '{translation}', '{lang_code}', 'SectionDto', 'system', NOW());"
            sql_lines.append(sql)
    return '\\n'.join(sql_lines)

